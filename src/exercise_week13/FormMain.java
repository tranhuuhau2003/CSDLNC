    private void jbtAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAddActionPerformed
            // TODO add your handling code here:
            //Lấy dữ liệu từ các trường đã nhập 
            String name = jtfName.getText();
            String email = jtfEmail.getText();
            String number = jtfNumber.getText();
            
            //Kiểm tra có bỏ trống không, sau đó thêm vào DB
            if (name.equals("")  && email.equals("") && number.equals("")) {
                JOptionPane.showMessageDialog(this, "Not input yet");
            } else {
                 Document document = new Document("Name", name)
                    .append("Email", email)
                    .append("Number",number);
            
                //insert into collection
                collection.insertOne(document);
                //load lại table
                loadTable();
            }
            //clear các trường
            clear();
    }//GEN-LAST:event_jbtAddActionPerformed

    private void jbtSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSelectActionPerformed
        // TODO add your handling code here:
        int row = jtbContact.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Not selected yet");
        } else {
            jtfName.setText(String.valueOf(jtbContact.getValueAt(row,1)));
            jtfEmail.setText(String.valueOf(jtbContact.getValueAt(row,2)));
            jtfNumber.setText(String.valueOf(jtbContact.getValueAt(row,3)));
            jbtUpdate.setVisible(true);
        }
    }//GEN-LAST:event_jbtSelectActionPerformed

    private void jbtUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtUpdateActionPerformed
        // TODO add your handling code here:
            int row = jtbContact.getSelectedRow();
            String id = String.valueOf(jtbContact.getValueAt(row,0));
            ObjectId objectId = new ObjectId(id);
            
            String name = jtfName.getText();
            String email = jtfEmail.getText();
            String number = jtfNumber.getText();
            
            //dữ liệu mới để update
            Document updateDocument = new Document("$set", new Document("Name",name).append("Email",email).append("Number",number));
            
            //điều kiện để xác định document cần update
            Document filterDocument = new Document("_id",objectId);
            
            //update 
            collection.updateOne(filterDocument, updateDocument);
            
            jbtUpdate.setVisible(false);
            loadTable();
            clear();
    }//GEN-LAST:event_jbtUpdateActionPerformed

