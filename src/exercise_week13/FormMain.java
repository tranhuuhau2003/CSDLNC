    private void jbtDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDeleteActionPerformed
        // TODO add your handling code here:
        int row = jtbContact.getSelectedRow();
        String id = String.valueOf(jtbContact.getValueAt(row,0));
        ObjectId objectId = new ObjectId(id);
        
        //điều kiện xác định document cần xóa 
        Document filterDocument = new Document("_id",objectId);

        int decision = JOptionPane.showConfirmDialog(this, "Delete?", "Select", JOptionPane.YES_NO_OPTION);
        if (decision == 0) {
            collection.deleteOne(filterDocument);
        }
        loadTable();
    }//GEN-LAST:event_jbtDeleteActionPerformed

